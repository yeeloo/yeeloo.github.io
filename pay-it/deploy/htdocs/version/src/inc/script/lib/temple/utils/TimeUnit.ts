/**
 * A collection of constants for date and time units.
 *
 * @module Temple
 * @namespace temple.utils
 * @class TimeUnit
 * @author Thijs Broerse
 */
class TimeUnit {
	/**
	 * A constant representing the year unit in date and time values.
	 *
	 * @static
	 * @property YEAR
	 * @type string
	 * @default 'year'
	 */
	public static YEAR:string = 'year';

	/**
	 * A constant representing the month unit in date and time values.
	 *
	 * @static
	 * @property MONTH
	 * @type string
	 * @default 'month'
	 */
	public static MONTH:string = 'month';

	/**
	 * A constant representing the day unit in date and time values.
	 *
	 * @static
	 * @property DAY
	 * @type string
	 * @default 'day'
	 */
	public static DAY:string = 'day';

	/**
	 * A constant representing the hours unit in date and time values.
	 *
	 * @static
	 * @property HOURS
	 * @type string
	 * @default 'hours'
	 */
	public static HOURS:string = 'hours';

	/**
	 * A constant representing the minutes unit in date and time values.
	 *
	 * @static
	 * @property MINUTES
	 * @type string
	 * @default 'minutes'
	 */
	public static MINUTES:string = 'minutes';

	/**
	 * A constant representing the seconds unit in date and time values.
	 *
	 * @static
	 * @property SECONDS
	 * @type string
	 * @default 'seconds'
	 */
	public static SECONDS:string = 'seconds';

	/**
	 * A constant representing the milliseconds unit in date and time values.
	 *
	 * @static
	 * @property MILLISECONDS
	 * @type string
	 * @default 'milliseconds'
	 */
	public static MILLISECONDS:string = 'milliseconds';
}

export default TimeUnit;